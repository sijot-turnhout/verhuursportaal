                        <h4>8) Uw rechten</h4>

                        <p>U heeft bepaalde rechten met betrekking tot uw persoonsgegevens, waaronder:</p>

                        <h5>Recht op inzage</h5>

                        <p>
                            Als bezoeker van onze website heeft u volgens de Algemene Verordening Gegevensbescherming (AVG) het recht om te weten welke
                            persoonlijke gegevens wij van u verzamelen, hoe wij deze gebruiken, met wie wij ze delen, en welke rechten u heeft met betrekking
                            tot uw gegevens.  Dit recht staat bekend als het "recht op inzage".
                        </p>

                        <h5>Recht op rectificatie</h5>

                        <p>
                            U heeft het recht om uw persoonlijke gegevens te corrigeren als deze onjuist of onvolledig zijn. Als u merkt dat de gegevens die wij
                            over u hebben correct zijn, kunt u ons verzoeken deze te wijzigen. Wij zullen zo snel mogelijk reageren op uw verzoek en de nodige aanpassing te doen.
                        </p>

                        <h6>Hoe kunt u een verzoek tot rectificatie indienen?</h6>

                        <ul>
                            <li><strong>Contact opnemen:</strong> Dit kan simpelweg door een email en of brief te verzenden naar de onderstaande contact gegevens.</li>
                            <li><strong>Verstrek de juiste informatie:</strong> Geef duidelijk aan welke gegevens onjuist of onvolledig zijn en verstrek de juiste informatie die moet worden gecorrigeerd.</li>
                            <li><strong>Identiteitsverificatie:</strong> Om uw privacy te beschermen, kunnen wij u vragen om uw identiteit te verifieren voordat wij uw verzoek kynnen verwerken.</li>
                        </ul>

                        <h6>Reactietijd</h6>

                        <p>
                            Wij streven ernaar om uw verzoek binnen 20 werkdagen te verwerken en u te informeren over de wijzigingen die zijn aangebracht.
                            In geval van complexe verzoeken kan het iets langer duren, maar wij houden u op de hoogte van de voortgang.
                        </p>

                        <h5>Recht op verwijdering</h5>

                        <p>
                            Als gebruiker van onze website hebt u het recht om te vragen dat wij uw persoonsgegevens verwijderen. Dit bestaan bekend als het "recht om vergeten te worden.
                            Dit recht geeft u de mogelijkheid om ons te verzoeken uw persoonlijke gegevens te wissen wanneer er geen legitieme reden meer is om deze te blijven verwerken.
                        </p>

                        <p>U kunt recht uitoefenen in de volgende situaties:</p>

                        <ul>
                            <li>De persoonsgegevens zijn niet langer nodig voor de doeleinden waarvoor ze zijn verzameld of verwerkt.</li>
                            <li>U trekt uw toestemming in waarop de verwerking is gebaseerd, en er is geen andere wettelijke grondslag voor de verwerking.</li>
                            <li>U maakt bezwaar tegen de verwerking en er zijn geen dwingende legitieme gronden voor de verwerking.</li>
                            <li>De persoonsgegevens zijn onrechtmatig verwerkt.</li>
                            <li>De persoonsgegevens moeten worden gewist om te voldoen aan een wettelijke verplichting.</li>
                        </ul>

                        <p>
                            Om uw recht op verwijdering uit te oefenen, kunt u contact met ons opnemen via de onderstaande contactgegevens. Wij zullen dan zo snel mogelijk, en in ieder geval
