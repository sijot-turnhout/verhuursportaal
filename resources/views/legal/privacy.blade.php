                        <p>Wij verzamelen persoonsgegevens op de volgende manieren:</p>

                        <ul class="list-unstyled">
                            <li><strong>Directe interacties:</strong> Wanneer u zich registreert, een profiel aanmaakt of gegevens invoert in de applicatie</li>
                            <li><strong>Automatische technologieen:</strong> Wanneer u de applicatie gebruikt, kunnen wij automatisch technische gegevens verzamelen</li>
