                            Als u gebruik wilt maken van uw recht op beperking van de verwerking, kunt u contact met ons opnemen via de onderstaande contactgegevens.
                            Wij zullen uw verzoek zo snel mogelijk in behandeling nemen en u informeren over de uitkomst.
                        </p>

