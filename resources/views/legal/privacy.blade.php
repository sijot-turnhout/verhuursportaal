                                Dit kan bijvoorbeeld een andere dienstverlener zijn. U kunt ons ook verzoeken om de gegevens rechtstreeks over te dragen aan dere andere
                                verwerkingsverantwoordelijke, indien dit technisch mogelijk is.
                            </li>
                        </ul>

