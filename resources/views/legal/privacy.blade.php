                            de gegevensnodig zijn voor het nakomen van een wettelijke verplichting of voor het instellen, uitoefenen of verdedigen van rechtsvorderingen.
                        </p>

                        <h5>Recht op beperking van de verwerking</h5>

                        <p>
                            Als gebruiker van onze wbesite heeft u het recht om de verwerking van uw persoonsgegevens te beperken in bepaalde situaties.
                            Dit betekent dat wij uw gegevens mogen opslaan, maar deze tijdelijk niet verder mogen verwerken of gebruiken.
                            U kunt dit recht uitoefenen in de volgende gevallen.
                        </p>

                        <dl>
                            <dt><strong>1. Nauwkeurigheid van gegevens betwisten</strong></dt>
                            <dd>
                                Als u van mening bent dat de persoonsgegevens die wij over u verwerken onjuist zijn,
                                kunt u ons verzoeken om de verwerking te beperken terwijl wij de neuwkeurigheid van de
                                gegevens verifieren.
                            </dd>

                            <dt><strong>2. Onrechtmatige verwerking</strong></dt>
                            <dd>
                                Als u denkt dat wij uw persoonsgegevens onrechtmatig verwerken, maar u niet wilt dat
                                deze gegevens worden gewist,
                                kunt u ons vragen om de verwerking te beperken.
                            </dd>

                            <dt><strong>3. Gegevens niet langer nodig, maar nodig voor juridische claims</strong></dt>
                            <dd>
                                Als wij yw persoonsgegevens niet langer nodig hebben voor de oorspronkelijke
                                verwerkingsdoeleinden,
                                maar u ze nodig heeft voor het instellen, uitoefenen of onderbowuen van een
                                rechtsvordering, kunt u verzoeken om beperking van de verwerking,
                            </dd>

                            <dt><strong>4. Bezwaar tegen verwerking</strong></dt>
                            <dd>
                                Als u bezwaar heeft gemaakt tegen de verwerking van uw persoonsgegevens in afwachting
                                van de verificatie of onze legitieme gronden
                                zwaarder wegen dan uw rechten.
                            </dd>
                        </dl>

