                        <p>Wij gebruiken uw persoonsgegevens voor de volgende doeleinden:</p>

                        <ul class="list-unstyled">
                            <li><strong>Verlenen van diensten:</strong> Om de applicatie te laten functioneren en u diensten te verlenen, zoals het beheren van verhuringen en huurcontracten.</li>
                            <li><strong>Klantenservice:</strong> Om u te ondersteunen bij vragen en problemen</li>
                            <li><strong>Betalingen verwerken:</strong> Om de veiligheid en integriteit van de applicatie te waarborgen.</li>
                            <li><strong>Wettelijke verplichtingen:</strong> Om te voldoen aan wettelijke verplichtingen en juridische processen.</li>
                            <li><strong>Verbetering van diensten:</strong> Om onze applicatie te analyseren en verbeteren.</li>
                        </ul>

                        <h4>5) Met wie delen wij uw gegevens?</h4>

                        <p>Wij kunnen uw persoonsgegevens delen met de volgende partijen:</p>

