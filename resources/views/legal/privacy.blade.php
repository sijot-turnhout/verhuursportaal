                            <li><strong>Wettelijke autoriteiten:</strong> Wanneer dit vereist is door wet- of regelgeving.</li>
                            <li><strong>Andere gebruikers:</strong> Binnen de context van de verhuursapplicatie, zoals beheerders, raad van bestuur (vzw), en leiding dat fungeert als vrijwilliger.</li>
